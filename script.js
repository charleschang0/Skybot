const getNested = (source, path) => path.split(".").reduce((value, key) => value?.[key], source);

function renderCompany(language) {
  const company = translations[language].company;
  const rowHost = document.querySelector("[data-company-rows]");
  const leaderHost = document.querySelector("[data-leaders]");

  rowHost.innerHTML = company.rows
    .map(([label, value]) => {
      const content = Array.isArray(value)
        ? `<ul>${value.map((item) => `<li>${item}</li>`).join("")}</ul>`
        : value;
      return `<div class="company-row"><dt>${label}</dt><dd>${content}</dd></div>`;
    })
    .join("");

  leaderHost.innerHTML = company.leaders
    .map(([role, name]) => `<div class="leader-card"><span>${role}</span><strong>${name}</strong></div>`)
    .join("");
}

function setLanguage(language) {
  const dict = translations[language] || translations.ja;
  document.documentElement.lang = language === "zh" ? "zh-Hant" : language;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = getNested(dict, node.dataset.i18n);
    if (typeof value === "string") node.textContent = value;
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.lang === language));
  });

  renderCompany(language);
  localStorage.setItem("skybot-language", language);
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

setLanguage(localStorage.getItem("skybot-language") || "ja");
